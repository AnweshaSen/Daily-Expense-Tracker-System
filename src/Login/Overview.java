    void showAvailableBalance(){
        try{
            String sql1="Select sum(income),sum(expense) from final";
            ps=con.prepareStatement(sql1);
            rs=ps.executeQuery();
            if(rs.next())
            {
                int sum_income=rs.getInt("sum(income)");
                int sum_expense=rs.getInt("sum(expense)");
                int avail=sum_income-sum_expense;
                String available=Integer.toString(avail);
                jLabelAB.setText(available);
            }
            
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
     
