    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String sql="Select sum(income),sum(expense) from final where DT=?";
            ps=con.prepareStatement(sql);
            Date d=jDateChooser1.getDate();
            java.sql.Date sqlDate = new java.sql.Date(d.getTime());
            ps.setDate(1, sqlDate);
            rs=ps.executeQuery();
            while(rs.next())
            {
                int inc=rs.getInt("sum(income)");
                int exp=rs.getInt("sum(expense)");
                jLabIncome.setText(Integer.toString(inc));
                jLabExp.setText(Integer.toString(exp));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "NO DATABASE FOUND");
        }
         
    }//GEN-LAST:event_jButton1ActionPerformed

